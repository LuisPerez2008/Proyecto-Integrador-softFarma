    public void validar() {
        String correo = txtCorreo.getText();
        String pass = String.valueOf(txtPass.getPassword());
        if (!"".equals(correo) || !"".equals(pass) ) {
            usarioLogin = login.log(correo, pass);
            
            if (usarioLogin != null) {
                if (usarioLogin.getCorreo() != null && usarioLogin.getPass() != null ) {
                Sistema sis = new Sistema(usarioLogin);
                sis.setVisible(true);
                dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Email o contrase침a incorrecta");
                }
            } else{
                 JOptionPane.showMessageDialog(null, "Email o contrase침a incorrecta");
            } 
        }else{
               JOptionPane.showMessageDialog(null, "El/Los campos estan vacios ");
        }
    }

